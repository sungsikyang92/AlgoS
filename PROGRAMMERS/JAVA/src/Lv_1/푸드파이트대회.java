package Lv_1;

public class 푸드파이트대회 {
    public String solution(int[] food) {
        String answer = "";
        int[] cnt = new int[(food.length - 1) * 2 + 1];
        for (int i = 0; i < cnt.length; i++) {
            if (i == cnt.length / 2) {
                cnt[i] = 0;
            }

        }
        return answer;
    }
}
